class Grocery{
    name: string;
    SKU: string;
    quantity: number;
    price: number;
    stock: string;

    constructor(name:string, sku:string, quantity:number,price:number,stock:string){
        this.name=name;
        this.SKU=sku;
        this.quantity=quantity;
        this.price=price;
        this.stock=stock;
    }
}